Operadores de comparaçõa (
=: Igual a.
<> ou !=: Diferente de.
<: Menor que.
>: Maior que.
<=: Menor ou igual a.
>=: Maior ou igual a.
)


Operadores lógicos (
AND: Retorna verdadeiro se ambas as condições forem verdadeiras.
OR: Retorna verdadeiro se pelo menos uma das condições for verdadeira.
NOT: Inverte o valor lógico da condição.
IN: verifica se um determinado valor está presente em uma lista de valores
BETWEEN: Verifica se um valor está dentro de um intervalo.
IS NULL: Verifica se um valor é nulo.
IS NOT NULL: Verifica se um valor não é nulo.
)

Operadores de texto LIKE -> Realiza uma correspondência parcial de texto usando curingas (% e _).

